function puzzle(input) {
    return _puzzle(input, 1, 1)
        * _puzzle(input, 3, 1)
        * _puzzle(input, 5, 1)
        * _puzzle(input, 7, 1)
        * _puzzle(input, 1, 2)
}

function _puzzle(input, dx, dy) {
    const map = new Map();
    let maxY;
    let maxX;
    input.split("\n").forEach((line, y) => {
        maxY = y;
        line.split("").forEach((char, x) => {
            map.set(`${x},${y}`, char === "#");
            maxX = x;
        });
    });
    // console.log(map);

    let x = 0;
    let y = 0;
    let treeCount = 0;
    while(y <= maxY) {
        x += dx;
        y += dy;
        if(map.get(`${x%(maxX+1)},${y}`)) {
            treeCount += 1;
        }
    }
    return treeCount;
}

